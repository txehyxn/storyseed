    @Override
    public GeneratedChapterResult generateOpening(Story story) {
        OpeningScene scene = createScene(story.getPrimaryGenre());
        String content = createOpening(scene, story.getSecondaryGenres());

        return new GeneratedChapterResult(
                content,
                List.of(scene.immediateChoice(), scene.investigativeChoice())
        );
    }

    private String createOpening(OpeningScene scene, List<Genre> secondaryGenres) {
        String secondaryAtmosphere = secondaryGenres.stream()
                .map(this::createSecondaryAtmosphere)
                .reduce("", (left, right) -> left + " " + right)
                .trim();

        return """
                %s

                %s

                %s

                %s%s

                %s

                %s
                """.formatted(
                scene.introduction(),
                scene.anomaly(),
                scene.reaction(),
                scene.escalation(),
                secondaryAtmosphere.isBlank() ? "" : " " + secondaryAtmosphere,
                scene.conflict(),
                scene.decision()
        ).trim();
    }

